package com.example.ikujetwende;

public class ActivityDetails {
    String activity,firstname,surname,description,location,date,time;

    public ActivityDetails() {
    }

    public ActivityDetails(String activity, String firstname, String surname, String description, String location, String date, String time) {
        this.activity = activity;
        this.firstname = firstname;
        this.surname = surname;
        this.description = description;
        this.location = location;
        this.date = date;
        this.time = time;
    }

    public String getActivity() {
        return activity;
    }

    public void setActivity(String activity) {
        this.activity = activity;
    }

    public String getFirstname() {
        return firstname;
    }

    public void setFirstname(String firstname) {
        this.firstname = firstname;
    }

    public String getSurname() {
        return surname;
    }

    public void setSurname(String surname) {
        this.surname = surname;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getLocation() {
        return location;
    }

    public void setLocation(String location) {
        this.location = location;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }
}
